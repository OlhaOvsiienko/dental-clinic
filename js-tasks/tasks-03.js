function giveMeFive(obj) {
    const arrOfFive = [];
    for (var key in obj) {
      if (key.length === 5) {
        arrOfFive.push(key);
      }
      if (obj[key].length === 5) {
        arrOfFive.push(obj[key]);
      }
    }
    return arrOfFive;
  }

